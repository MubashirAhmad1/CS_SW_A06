    private void changePasswordButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_changePasswordButtonActionPerformed
        DAL dal=new DAL();
        
        //getting passwords password fields
        char[] password;
        password=curPasswordField.getPassword();
        String curPassword="";
        for(int i=0;i<password.length;i++){
           curPassword+=password[i]; 
        }
        
        password=newPasswordField.getPassword();
        String newPassword="";
        for(int i=0;i<password.length;i++){
           newPassword+=password[i]; 
        }
        
        password=confirmNewPasswordField.getPassword();
        String confirmPassword="";
        for(int i=0;i<password.length;i++){
           confirmPassword+=password[i]; 
        }
        
        dal.rs=dal.result("select * from users where userId=100 and password='"+curPassword+"'");
        
        try{
        dal.rs.next();
        
        if(dal.rs.getRow()==1){
            
            if(newPassword.equals(confirmPassword)){
                
                if(dal.myQuery("UPDATE users SET password='"+newPassword+"' WHERE userId=100")){
                    dal.show("Password Changed Succssfully");
                }
                
            }
            else{
                javax.swing.JOptionPane.showMessageDialog(null,"Passwords must be same");
                
            }
        }
        else{
            javax.swing.JOptionPane.showMessageDialog(null,"Incorrect Password");
        }
        }catch(Exception e){
                
        }
    }//GEN-LAST:event_changePasswordButtonActionPerformed

